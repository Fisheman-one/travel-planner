export function getMockItinerary(
  city: string,
  days: number
): FullPlanResult | null {
  const cityItineraries = MOCK_ITINERARIES[city];
  if (!cityItineraries) return null;

  // 优先返回精确天数，如果没有则返回最接近的
  if (cityItineraries[days]) {
    return cityItineraries[days];
  }

  // 返回最接近的天数
  const availableDays = Object.keys(cityItineraries).map(Number);
  const closest = availableDays.reduce((prev, curr) =>
    Math.abs(curr - days) < Math.abs(prev - days) ? curr : prev
  );

  return cityItineraries[closest];
}

